package com.edsys.app.user.form;

public class Announcements {
	
	private int eventId;
	
	private String eventName;
	
	private String studentClass;
	
	private String studentSection;
	
	private String annoncedDate;
	
	private String eventGoingToHappen;
	
	private String discription;

	private String eventForStaff;
	

	public int getEventId() {
		return eventId;
	}

	public void setEventId(int eventId) {
		this.eventId = eventId;
	}

	public String getEventForStaff() {
		return eventForStaff;
	}

	public void setEventForStaff(String eventForStaff) {
		this.eventForStaff = eventForStaff;
	}

	public String getEventName() {
		return eventName;
	}

	public void setEventName(String eventName) {
		this.eventName = eventName;
	}

	public String getStudentClass() {
		return studentClass;
	}

	public void setStudentClass(String studentClass) {
		this.studentClass = studentClass;
	}

	public String getStudentSection() {
		return studentSection;
	}

	public void setStudentSection(String studentSection) {
		this.studentSection = studentSection;
	}

	public String getAnnoncedDate() {
		return annoncedDate;
	}

	public void setAnnoncedDate(String annoncedDate) {
		this.annoncedDate = annoncedDate;
	}

	public String getDiscription() {
		return discription;
	}

	public void setDiscription(String discription) {
		this.discription = discription;
	}

	public String getEventGoingToHappen() {
		return eventGoingToHappen;
	}

	public void setEventGoingToHappen(String eventGoingToHappen) {
		this.eventGoingToHappen = eventGoingToHappen;
	}
	
	
	

}
